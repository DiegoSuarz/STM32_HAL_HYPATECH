uint8_t readMessageUART(void)
{
	 char bufferRx[1];
	 HAL_UART_Transmit(&huart2,(uint8_t*)PROMPT ,strlen(PROMPT), HAL_MAX_DELAY);
	 HAL_UART_Receive(&huart2, (uint8_t*)bufferRx,1, HAL_MAX_DELAY);
	 HAL_Delay(50);
	 return atoi(bufferRx);
}

uint8_t proccesMenssage(uint8_t dt)
{
	char msg[30];

	if(!dt || dt > 3)
		return 0;

	sprintf(msg,"%d",dt);
	HAL_UART_Transmit(&huart2,(uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);

	switch(dt)
	{
	case 1:
		HAL_GPIO_TogglePin(LED_USER_GPIO_Port, LED_USER_Pin);
		break;
	case 2:
		sprintf(msg,"\r\nUSER BUTTON STATUS: %s",HAL_GPIO_ReadPin(USER_BUTTON_GPIO_Port, USER_BUTTON_Pin)== GPIO_PIN_RESET ? "PRESIONADO" : "LIBERADO");
		HAL_UART_Transmit(&huart2, (uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);
		break;
	case 3:
			return 2;
	}


	return 1;
}
